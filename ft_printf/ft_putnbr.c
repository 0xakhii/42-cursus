#include "ft_printf.h"

void	ft_putnbr(int c)
{
	if (c >= 0 && c <= 9)
		ft_putchar(c);
	if (c > 9)
	{
		ft_putnbr(c % 10);
		ft_putnbr(c / 10);
	}
	if (c == -2147483648)
		write(1, "-2147483648", 12);
	else
	{
		ft_putchar('-');
		ft_putnbr(c * -1);
	}
}
