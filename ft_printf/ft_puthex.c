#include "ft_printf.h"

void	ft_puthex(int c, int *len)
{
	char	*hex;

	hex = "0123456789abcdef";
	if (c < 16)
	{
		ft_putchar(hex[c], *len);
	}
	else
	{
		ft_putchar(c / 16, *len);
		ft_putchar(c % 16 + '0', *len);
	}
}
