void	ft_puthex(int c, int upper)
{
	char	*hex;
	char	*hex_upper;

	hex = "0123456789abcdef";
	hex_upper = "0123456789ABCDEF";
	if (upper)
		c = hex;
	else
		c = hex_upper;
}
